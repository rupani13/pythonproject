# All Identity Program

var1 = int(input("Enter the first value"))
var2 = int(input("Enter the second value"))

list1 = [10, 12, 13, 15]

Var = var1 is not var2
Fun = var1 is var2

print("Is Not Value:", Var)
print("Is Value:", Fun)

# Membership Operators
if var1 not in list1:
    print("var1 is NOT present in given list1")
else:
    print("var1 is  present in given list1")

if var2 in list1:
    print("var2 is present in given list1")
else:
    print("var2 is Not present in given list1")


