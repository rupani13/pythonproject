# All Logical Program

var1 = int(input("Enter the first value"))
var2 = int(input("Enter the second value"))

Var = var1 and var2     # Logical AND
Fun = var1 or var2      # Logical OR
Obj = not var1          # Logical Not


print("Logical AND Value:", Var)
print("Logical OR Value:", Fun)
print("Logical Not Value:", Obj)

