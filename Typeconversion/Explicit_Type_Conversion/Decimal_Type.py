# Explicit Type Conversion Program (Oct, Hex, Ord)

# For  ord(Character to integer)

value1 = "9"
value2 = ord(value1)
print("Character to integer value2:", value2)

# For hex(integer converting to hexadecimal)

value3 = hex(50)
print("Integer converting to Hexadecimal value3:", value3)

# For oct(integer converting to Octal)

value4 = oct(45)
print("Integer converting to Octal value4:", value4)
