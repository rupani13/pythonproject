# Explicit Type Conversion Program (List, tuple,Set)

# For Tuple(string converting to tuple)
value1 = 'The_Book'
value2 = tuple(value1)
print("string converting to tuple:", value2)
print("string converting to tuple Type:", type(value2))
print("string converting to tuple Length:", len(value2))

# For List(string converting to List)
Value3 = list(value1)
print("string converting to List:", Value3)
print("string converting to list Type:", type(Value3))
print("string converting to List Length:", len(Value3))

# For Set(string converting to Set)
Value4 = set(value1)
print("string converting to Set:", Value4)
print("string converting to Set Type:", type(Value4))
print("string converting to Set Length:", len(Value4))
