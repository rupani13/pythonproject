# Explicit Type Conversion Program (Dictionary, Str, Complex)

# For Dictionary(string converting to Dictionary)

tup = (('class', 8), ('student', 15), ('teacher', 2))
value3 = dict(tup)
print("string converting to tuple:", value3)
print("string converting to tuple Type:", type(value3))
print("string converting to tuple Length:", len(value3))

